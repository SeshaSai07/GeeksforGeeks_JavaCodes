public class eightyfour 
{
    public static void main(String[] args) 
    {
        //intialize the array
        int [] arr = {40 , 50 , 89 , 98 , 90 , 67, 42};

        //size of array
        int n = arr.length;

        //traversing array
        for (int i = 0 ; i < n; i++) {
            System.out.println(arr[i] + " ");
        }
    }
    
}
